def build_ddj_lexicon() -> list[dict[str, str]]:
    lexicon = []
    for term, _, _ in DDJ_CONCEPTS:
        lexicon.append({"term": term, "type": "concept"})
    for term in DDJ_EXTRA_CONCEPTS:
        lexicon.append({"term": term, "type": "concept"})
    for term, _ in DDJ_IDENTITIES:
        lexicon.append({"term": term, "type": "identity"})
    for term in DDJ_QUANTITIES:
        lexicon.append({"term": term, "type": "quantity"})
    for term in DDJ_PERSONS:
        lexicon.append({"term": term, "type": "person"})
    for term in DDJ_OBJECTS:
        lexicon.append({"term": term, "type": "object"})
